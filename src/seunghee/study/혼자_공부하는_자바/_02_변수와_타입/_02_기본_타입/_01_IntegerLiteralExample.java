package seunghee.study.혼자_공부하는_자바._02_변수와_타입._02_기본_타입;

public class _01_IntegerLiteralExample {
	public static void main(String[] args) {
		int var1 = 0b1011; 	// 2진수
		int var2 = 0206;	// 8진수
		int var3 = 365; 	// 10진수
		int var4 = 0xB3;	// 16진수
		
		System.out.println("var1 : "+var1);
		System.out.println("var2 : "+var2);
		System.out.println("var3 : "+var3);
		System.out.println("var4 : "+var4);
	}
}
