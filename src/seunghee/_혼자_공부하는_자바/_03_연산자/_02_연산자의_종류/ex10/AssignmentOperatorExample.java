package seunghee._혼자_공부하는_자바._03_연산자._02_연산자의_종류.ex10;

public class AssignmentOperatorExample {
	public static void main(String[] args) {
		int result = 0;
		result += 10;
		System.out.println("result = "+result);
		result += 10;
		System.out.println("result = "+result);
		result *= 3;
		System.out.println("result = "+result);
		result /= 5;
		System.out.println("result = "+result);
		result %= 3;
		System.out.println("result = "+result);
	}
}
